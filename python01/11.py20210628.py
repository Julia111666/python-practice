a=float(input('請輸入依成績'))
if ((a>=90)and(a<=100)):
    print('%5.2f 分成績為優等' %(a))
elif ((a>=80)and(a<90)):
     print('%d 分成績為甲等' %(a))
elif ((a>=70)and(a<80)):
     print('%d 分成績為乙等' %(a))
elif ((a>=60)and(a<70)):
     print('%d 分成績為丙等' %(a))
elif ((a>=50)and(a<60)):
     print('%d 分成績為丁等' %(a))
else :
     print('您輸入的成績分數不在正常範圍內')
     
    

  
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    